
public class BitwiseOperators {

	public static void main(String[] args) {
		
		        int a = 5; // Binary: 0101
		        int b = 3; // Binary: 0011

		        // Bitwise AND
		        System.out.println("a & b: " + (a & b)); // Result: 0001 (decimal 1)

		        // Bitwise OR
		        System.out.println("a | b: " + (a | b)); // Result: 0111 (decimal 7)

		        // Bitwise XOR
		        System.out.println("a ^ b: " + (a ^ b)); // Result: 0110 (decimal 6)

		        // Bitwise NOT
		        System.out.println("~a: " + (~a)); // Result:  (32-bit representation of -6)
		    }
		

	

}
